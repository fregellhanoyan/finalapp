deleteSong(key){
    let r = this.songApp;
    for(let i=0;i<r.length;i++){
      if(r[i].songid == key){
        this.songApp.splice(i,1);
        break;
      }
    }   
    this.listSong();
  }

  idSearch(songid){
    let r = this.songApp;
    for(let i=0;i<r.length;i++){
      if(songid==r[i].songid){
        return r[i];
      }
    }
  } 

  songSearch(songtitle){
    let objects = [];
    let r = this.songApp;
    for(let i=0;i<r.length;i++){
      let expr = (r[i].songtitle.toUpperCase().indexOf(songtitle.toUpperCase()) > -1);
      if(expr){
        objects.push(r[i]);
      }
    }
    return objects;
  }



